package Model;

/**
 *
 * @author Admin
 */
public class HotelLikeLog {
    private int id;
    private int status;
    private int userId;
    private int hotelId;

    public HotelLikeLog() {
    }

    public HotelLikeLog(int id, int status, int userId, int hotelId) {
        this.id = id;
        this.status = status;
        this.userId = userId;
        this.hotelId = hotelId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getHotelId() {
        return hotelId;
    }

    public void setHotelId(int hotelId) {
        this.hotelId = hotelId;
    }    
}
